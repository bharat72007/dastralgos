package com.java.arrays;

class Tuple {
	
	private int i;
	private int j;
	private String str;
	public Tuple(int i, int j, String str){
		this.i = i;
		this.j = j;
		this.str = str;
	}
	public int getI() {
		return i;
	}
	
	public int getJ() {
		return j;
	}
	
	public String getString() {
		return str;
	}
	
}
